#define dbg(...) cerr << "(" << #__VA_ARGS__ << "):", dbg_out(__VA_ARGS__)
void dbg_out() { cerr << endl; }
template <typename Head, typename... Tail>
void dbg_out(Head H, Tail... T) {
  cerr << ' ' << H;
  dbg_out(T...);
}

const int N = 60;
int n, a[N];

void solve() {
  cin >> n;
  int va = 0, vb = 0;
  for (int i = 0; i < n; i++) {
    cin >> a[i];
    if (a[i] == 2)
      vb++;
    else
      va++;
  }
  cout << va << endl;
}

int main() {
  ios::sync_with_stdio(0);
  cin.tie(0);
  int t;
  cin >> t;
  while (t--) {
    solve();
  }
  return 0;
}